#include <iostream>
#include <math.h>
using namespace std;

int main()
{
    cout << "Please enter a floating-point value: \n";
    double numB;
    cin >> numB;
    cout << "Number is " << numB << endl;
    cout << "The number plus one is "  << numB + 1 << endl;
    cout << " Three times this number is " << 3*numB << endl
         << " Double this number is " << numB + numB << endl
         << " This number squared is " << numB * numB << endl
         << " Half of this number is " << numB/2 << endl
         << "The square root of this number is " << sqrt(numB)
         << endl;



























}
